/**
 *Checks whether the given object has the specified property.
 * @param obj The object to check.
 * @param key The key to check.
 * @returns true if property found, false otherwise.
 */
 export function codemelted_check_has_property(
    obj: object, key: string
): boolean {
    return Object.hasOwn(obj, key);
}

